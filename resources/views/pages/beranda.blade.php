<p><strong>KUDAMAS (Kuningan Dalam Angka Statistik)</strong> adalah sebuah aplikasi android yang didesain untuk mengembangkan informasi mengenai Badan Pusat Statistik Kabupaten Kuningan di jaman milenial saat ini.</p>

<p>Aplikasi ini hadir untuk melengkapi website milik Badan Pusat Statistik Kabupaten Kuningan yang saat ini ada agar memberikan informasi yang lebih komprehensif dan memudahkan para pengguna data statistik khususnya terkait indikator-indikator/data makro Kabupaten Kuningan.</p>

<p>Indikator-indikator/data makro kabupaten kuningan yang tersaji dapat diakses secara offline (tanpa jaringan internet) maupun secara online (dengan jaringan internet).</p>

<p>Indikator-indikaator/data makro kabupaten kuningan yang tersaji dapat diakses secara offline (tanpa jaringan internet) mupun secara online (dengan jaringan internet) terutama data yang terhubung ke website-website yang dimuat dalam aplikasi.</p>

<p>Aplikasi ini akan di-update setiap tahun dan apabila terdapat perubahan atau pembaharuan data agar indikator-indikator/data yang disajikan tetap mutakhir </p>

<p>Akhir kata semoga aplikasi ini dapat bermanfaat bagi seluruh pengguna data BPS Kabupaten Kuningan sebagai salah satu upaya kami untuk mewujudkan pelayanan publik yang prima dan konsisten </p>
