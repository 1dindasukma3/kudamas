<div style="color:#2B2B2B; line-height:1.9; font-size:15px; text-align:justify;">

    <h5 class="fw-bold mt-2" style="color:#3A3A3A;">
        1. Penduduk usia kerja
    </h5>

    <p>
        Penduduk usia kerja adalah penduduk berumur 15 tahun dan lebih.
    </p>

    <h5 class="fw-bold mt-4" style="color:#3A3A3A;">
        2. Penduduk yang termasuk angkatan kerja
    </h5>

    <p>
        Penduduk yang termasuk angkatan kerja adalah penduduk usia kerja (15 tahun dan lebih) yang bekerja, atau punya pekerjaan namun sementara tidak bekerja dan pengangguran.
    </p>

    <h5 class="fw-bold mt-4" style="color:#3A3A3A;">
        3. Penduduk yang termasuk bukan angkatan kerja
    </h5>

    <p>
        Penduduk yang termasuk bukan angkatan kerja adalah penduduk usia kerja (15 tahun dan lebih) yang masih sekolah, mengurus rumah tangga atau melaksanakan kegiatan lainnya selain kegiatan pribadi.
    </p>

    <h5 class="fw-bold mt-4" style="color:#3A3A3A;">
        4. Bekerja
    </h5>

    <p>
        Bekerja adalah kegiatan ekonomi yang dilakukan oleh seseorang dengan maksud memperoleh atau membantu memperoleh pendapatan atau keuntungan paling sedikit 1 jam (satu jam) secara tidak terputus selama seminggu yang lalu. Kegiatan tersebut termasuk pula kegiatan pekerja tak dibayar yang membantu dalam suatu usaha/kegiatan ekonomi.
    </p>

    <h5 class="fw-bold mt-4" style="color:#3A3A3A;">
        5. Punya pekerjaan tetapi sementara tidak bekerja
    </h5>

    <p>
        Punya pekerjaan tetapi sementara tidak bekerja adalah keadaan seseorang yang mempunyai pekerjaan tetapi selama seminggu yang lalu sementara tidak bekerja karena berbagai sebab seperti sakit, cuti, menunggu panen, mogok kerja dan sebagainya.
    </p>

    <h5 class="fw-bold mt-4" style="color:#3A3A3A;">
        6. Penganggur terbuka
    </h5>

    <p>
        Penganggur terbuka terdiri dari:
    </p>

    <p>
        a. Mereka yang tak punya pekerjaan dan mencari pekerjaan.
        <br>
        b. Mereka yang tak punya pekerjaan dan mempersiapkan usaha.
        <br>
        c. Mereka yang tak punya pekerjaan dan tidak mencari pekerjaan karena merasa tidak mungkin mendapatkan pekerjaan.
        <br>
        d. Mereka yang sudah punya pekerjaan tetapi belum mulai bekerja.
    </p>

    <h5 class="fw-bold mt-4" style="color:#3A3A3A;">
        7. Tingkat Pengangguran Terbuka (TPT)
    </h5>

    <p>
        Tingkat Pengangguran Terbuka adalah persentase jumlah pengangguran terhadap jumlah angkatan kerja.
    </p>

    <h5 class="fw-bold mt-4" style="color:#3A3A3A;">
        8. Pekerja penuh
    </h5>

    <p>
        Pekerja penuh adalah penduduk bekerja dengan jumlah jam kerja minimal 35 jam selama seminggu.
    </p>

    <h5 class="fw-bold mt-4" style="color:#3A3A3A;">
        9. Setengah pengangguran
    </h5>

    <p>
        Setengah pengangguran adalah penduduk bekerja yang jam kerjanya kurang dari 35 jam seminggu.
    </p>

    <h5 class="fw-bold mt-4" style="color:#3A3A3A;">
        10. Sekolah
    </h5>

    <p>
        Sekolah adalah kegiatan seseorang untuk bersekolah di sekolah formal maupun non formal pada pendidikan dasar, menengah dan tinggi.
    </p>

    <h5 class="fw-bold mt-4" style="color:#3A3A3A;">
        11. Mengurus rumah tangga
    </h5>

    <p>
        Mengurus rumah tangga adalah kegiatan seseorang yang mengurus rumah tangga tanpa mendapatkan upah.
    </p>

    <h5 class="fw-bold mt-4" style="color:#3A3A3A;">
        12. Kegiatan lainnya
    </h5>

    <p>
        Kegiatan lainnya adalah kegiatan selain bekerja, sekolah dan mengurus rumah tangga.
    </p>

    <h5 class="fw-bold mt-4" style="color:#3A3A3A;">
        13. Pendidikan tertinggi yang ditamatkan
    </h5>

    <p>
        Pendidikan tertinggi yang ditamatkan adalah tingkat pendidikan tertinggi yang berhasil diselesaikan.
    </p>

    <h5 class="fw-bold mt-4" style="color:#3A3A3A;">
        14. Jumlah jam kerja seluruh pekerjaan
    </h5>

    <p>
        Jumlah jam kerja seluruh pekerjaan adalah lamanya waktu bekerja dari seluruh pekerjaan yang dilakukan selama seminggu.
    </p>

    <h5 class="fw-bold mt-4" style="color:#3A3A3A;">
        15. Lapangan usaha
    </h5>

    <p>
        Lapangan usaha adalah bidang kegiatan dari pekerjaan/tempat bekerja/perusahaan/kantor tempat seseorang bekerja.
    </p>

    <h5 class="fw-bold mt-4" style="color:#3A3A3A;">
        16. Jenis pekerjaan/jabatan utama
    </h5>

    <p>
        Jenis pekerjaan/jabatan utama adalah macam pekerjaan yang dilakukan seseorang atau ditugaskan kepada seseorang yang sedang bekerja atau sementara tidak bekerja.
    </p>

    <h5 class="fw-bold mt-4" style="color:#3A3A3A;">
        17. Status pekerjaan utama
    </h5>

    <p>
        Status pekerjaan utama adalah kedudukan seseorang dalam melakukan pekerjaan di suatu unit usaha/kegiatan.
    </p>

    <p>
        <strong>a. Berusaha sendiri</strong> adalah bekerja atau berusaha dengan menanggung risiko secara ekonomis, yaitu dengan tidak kembalinya ongkos produksi yang telah dikeluarkan dalam rangka usahanya tersebut, serta tidak menggunakan pekerja dibayar maupun pekerja tak dibayar.
    </p>

    <p>
        <strong>b. Berusaha dibantu buruh tidak tetap/buruh tidak dibayar</strong> adalah bekerja atau berusaha atas risiko sendiri dan menggunakan buruh/pekerja tak dibayar dan atau buruh/pekerja tidak tetap.
    </p>

    <p>
        <strong>c. Berusaha dibantu buruh tetap/buruh dibayar</strong> adalah berusaha atas risiko sendiri dan mempekerjakan paling sedikit satu orang buruh/karyawan/pegawai tetap yang dibayar.
    </p>

    <p>
        <strong>d. Buruh/Karyawan/Pegawai</strong> adalah seseorang yang bekerja pada orang lain atau instansi/kantor/perusahaan secara tetap dengan menerima upah/gaji baik berupa uang maupun barang. Buruh yang tidak mempunyai majikan tetap, tidak digolongkan sebagai buruh/karyawan, tetapi sebagai pekerja bebas. Seseorang dianggap memiliki majikan tetap jika memiliki 1 (satu) majikan (orang/rumah tangga) yang sama dalam sebulan terakhir, khusus pada sektor bangunan batasannya tiga bulan. Apabila majikannya instansi/lembaga, boleh lebih dari satu.
    </p>

    <p>
        <strong>e. Pekerja bebas di pertanian</strong> adalah seseorang yang bekerja pada orang lain/majikan/institusi yang tidak tetap (lebih dari 1 majikan dalam sebulan terakhir) di usaha pertanian baik berupa usaha rumah tangga maupun bukan usaha rumah tangga atas dasar balas jasa dengan menerima upah atau imbalan baik berupa uang maupun barang, dan baik dengan sistem pembayaran harian maupun borongan. Usaha pertanian meliputi pertanian tanaman pangan, perkebunan, kehutanan, peternakan, perikanan dan perburuan, termasuk juga jasa pertanian.
    </p>

    <p>
        <strong>f. Majikan</strong> adalah orang atau pihak yang memberikan pekerjaan dengan pembayaran yang disepakati.
    </p>

    <p>
        <strong>g. Pekerja bebas di nonpertanian</strong> adalah seseorang yang bekerja pada orang lain/majikan/institusi yang tidak tetap (lebih dari 1 majikan dalam sebulan terakhir), di usaha non pertanian dengan menerima upah atau imbalan baik berupa uang maupun barang dan baik dengan sistem pembayaran harian maupun borongan. Usaha non pertanian meliputi: usaha di sektor pertambangan, industri, listrik, gas dan air, sektor konstruksi/bangunan, sektor perdagangan, sektor angkutan, pergudangan dan komunikasi, sektor keuangan, asuransi, usaha persewaan bangunan, tanah dan jasa perusahaan, sektor jasa kemasyarakatan, sosial dan perorangan.
    </p>

    <p>
        <strong>h. Pekerja keluarga/tak dibayar</strong> adalah seseorang yang bekerja membantu orang lain yang berusaha dengan tidak mendapat upah/gaji, baik berupa uang maupun barang.
    </p>

