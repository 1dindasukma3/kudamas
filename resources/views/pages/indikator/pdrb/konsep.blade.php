    <h5 class="fw-bold mt-2" style="color:#3A3A3A;">
        1. Wilayah Domestik dan Regional
    </h5>

    <p>
        Pengertian domestik, yang sering dipakai menunjukkan suatu wilayah (lokasi) dari suatu kesatuan ekonomi yang dapat berupa rumah tangga, perusahaan, pemerintah dan lain sebagainya. Regional merupakan suatu daerah tertentu yang memiliki batas administrasi tertentu.
    </p>

    <h5 class="fw-bold mt-4" style="color:#3A3A3A;">
        2. Produk Domestik
    </h5>

    <p>
        Produk domestik merupakan seluruh barang dan jasa yang dihasilkan oleh berbagai kegiatan ekonomi yang berada di dalam wilayah domestik suatu daerah tanpa memperhatikan apakah faktor produksinya berasal dari daerah tersebut atau dari luar daerah. Produk domestik mencakup hasil produksi yang dilakukan oleh penduduk maupun bukan penduduk yang berada di wilayah tersebut.
    </p>

    <h5 class="fw-bold mt-4" style="color:#3A3A3A;">
        3. Produk Regional
    </h5>

    <p>
        Produk regional merupakan produk yang ditimbulkan oleh faktor produksi yang dimiliki oleh penduduk suatu daerah tertentu tanpa memperhatikan lokasi produksinya. Dengan demikian produk regional dapat terjadi di luar wilayah daerah tersebut selama faktor produksinya dimiliki oleh penduduk daerah yang bersangkutan.
    </p>

    <h5 class="fw-bold mt-4" style="color:#3A3A3A;">
        4. Produk Domestik Regional
    </h5>

    <p>
        Produk Domestik Regional merupakan jumlah nilai tambah yang dihasilkan oleh seluruh unit usaha dalam suatu daerah tertentu atau merupakan jumlah nilai barang dan jasa akhir yang dihasilkan oleh seluruh unit ekonomi pada suatu wilayah.
    </p>

    <h5 class="fw-bold mt-4" style="color:#3A3A3A;">
        5. Produk Domestik Regional Bruto (PDRB) Atas Dasar Harga Berlaku
    </h5>

    <p>
        Produk Domestik Regional Bruto atas dasar harga berlaku adalah nilai tambah barang dan jasa yang dihitung menggunakan harga yang berlaku pada setiap tahun. PDRB atas dasar harga berlaku digunakan untuk melihat struktur ekonomi dan kemampuan sumber daya ekonomi suatu daerah.
    </p>

    <h5 class="fw-bold mt-4" style="color:#3A3A3A;">
        6. Produk Domestik Regional Neto (PDRN) Atas Dasar Harga Pasar
    </h5>

    <p>
        PDRN atas dasar harga pasar diperoleh dari PDRB atas dasar harga pasar dikurangi penyusutan. Penyusutan yang dimaksud adalah nilai susut barang modal tetap yang digunakan dalam proses produksi selama periode tertentu.
    </p>

    <h5 class="fw-bold mt-4" style="color:#3A3A3A;">
        7. Produk Domestik Regional Neto (PDRN) Atas Dasar Biaya Faktor
    </h5>

    <p>
        Produk Domestik Regional Neto atas dasar biaya faktor diperoleh dari PDRN atas dasar harga pasar dikurangi pajak tidak langsung netto. Nilai ini menunjukkan balas jasa terhadap faktor-faktor produksi yang ikut serta dalam proses produksi.
    </p>

    <h5 class="fw-bold mt-4" style="color:#3A3A3A;">
        8. Pendapatan Regional
    </h5>

    <p>
        Pendapatan regional merupakan jumlah balas jasa yang diterima oleh faktor produksi yang ikut serta dalam proses produksi di suatu daerah dalam jangka waktu tertentu. Pendapatan regional sering digunakan untuk menggambarkan tingkat pendapatan masyarakat suatu daerah.
    </p>

    <h5 class="fw-bold mt-4" style="color:#3A3A3A;">
        9. Pendapatan Regional Perkapita
    </h5>

    <p>
        Pendapatan regional perkapita diperoleh dengan membagi pendapatan regional dengan jumlah penduduk pertengahan tahun. Indikator ini digunakan untuk melihat tingkat kesejahteraan masyarakat secara rata-rata.
    </p>

